import mongoose from "mongoose";

// Define the Comment schema
const CommentSchema = new mongoose.Schema(
  {
    comment: {
      type: String,
      required: true,
    },
    author: {
      type: String,
      required: true,
    },
    postId: {
      type: String,
      required: true,
    },
    userId: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

// Create and export the Comment model based on the schema
export default mongoose.model("Comment", CommentSchema);
